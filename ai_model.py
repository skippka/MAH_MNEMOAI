        results: List[Dict] = []
        
        if not phrases:
            return results
        
        # –ë–µ—Ä–µ–º–æ 3‚Äë7 –Ω–∞–π–≤–∞–∂–ª–∏–≤—ñ—à–∏—Ö —Ñ—Ä–∞–∑
        selected = phrases[:7]
        
