    def generate_quiz(self, text: str) -> List[Dict]:
        """–ì–µ–Ω–µ—Ä–∞—Ü—ñ—è —ñ–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–æ–≥–æ —Ç–µ—Å—Ç—É –Ω–∞ –æ—Å–Ω–æ–≤—ñ —Ç–µ–∫—Å—Ç—É"""
        questions = []
        
        if not text or len(text) < 50:
            return questions
        
        # –ü—Ä–æ—Å—Ç–∏–π –∞–ª–≥–æ—Ä–∏—Ç–º –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü—ñ—ó –ø–∏—Ç–∞–Ω—å
        sentences = [s.strip() for s in text.split('.') if len(s.strip()) > 20]
        
        for i, sentence in enumerate(sentences[:7]):  # –û–±–º–µ–∂—É—î–º–æ 7 –ø–∏—Ç–∞–Ω–Ω—è–º–∏
            words = sentence.split()
            if len(words) > 5:
                # –í–∏–±–∏—Ä–∞—î–º–æ –∫–ª—é—á–æ–≤–µ —Å–ª–æ–≤–æ –¥–ª—è –∑–∞–º—ñ–Ω–∏
                if len(words) > 3:
                    key_index = min(3, len(words) - 2)
                    key_word = words[key_index]
                else:
                    key_word = words[0]
                
                # –°—Ç–≤–æ—Ä—é—î–º–æ –ø–∏—Ç–∞–Ω–Ω—è –∑ –ø—Ä–æ–ø—É—Å–∫–æ–º
                question_words = words.copy()
                question_words[key_index] = "_____"
                question_text = ' '.join(question_words) + '?'
                
                # –ì–µ–Ω–µ—Ä—É—î–º–æ –≤–∞—Ä—ñ–∞–Ω—Ç–∏ –≤—ñ–¥–ø–æ–≤—ñ–¥–µ–π
                options = [key_word]
                
                # –î–æ–¥–∞—î–º–æ —ñ–Ω—à—ñ —Å–ª–æ–≤–∞ –∑ —Ä–µ—á–µ–Ω–Ω—è (–∫—Ä—ñ–º —Å—Ç–æ–ø-—Å–ª—ñ–≤)
                other_words = [w.strip('.,!?') for w in words if w.strip('.,!?') != key_word and len(w) > 3]
                random.shuffle(other_words)
                
                # –î–æ–¥–∞—î–º–æ 3 –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏
                for wrong_word in other_words[:3]:
                    if wrong_word not in options and len(options) < 4:
                        options.append(wrong_word)
                
                # –Ø–∫—â–æ –Ω–µ –≤–∏—Å—Ç–∞—á–∏–ª–æ –≤–∞—Ä—ñ–∞–Ω—Ç—ñ–≤, –¥–æ–¥–∞—î–º–æ –Ω–æ–≤—ñ
                while len(options) < 4:
                    fake_word = f"–≤–∞—Ä—ñ–∞–Ω—Ç{len(options)}"
                    options.append(fake_word)
                
                random.shuffle(options)
                
                questions.append({
                    'id': i + 1,
                    'question': question_text,
                    'options': options,
                    'correct': key_word,
                    'explanation': f'–ü—Ä–∞–≤–∏–ª—å–Ω–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥—å: "{key_word}" - –∫–ª—é—á–æ–≤–∏–π —Ç–µ—Ä–º—ñ–Ω –∑ —Ç–µ–∫—Å—Ç—É',
                    'difficulty': '—Å–µ—Ä–µ–¥–Ω—è'
                })
        
        return questions
    
